package com.fredericboisguerin.pdf.ui.datasheet.extract;

class RawPoint {
    private final float x;
    private final float y;

    public RawPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
